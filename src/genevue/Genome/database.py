"""
An extensible genomic database based on file objects and featuring a fixed interaction interface.

The implementation is very straightforward:
1. Assign a UUID to each file and folder; if necessary, compress sequence files into .bgz format, convert GFF files to
   Parquet databases, or generate CDS and PEP sequences based on the specified codon type
2. Use a single-node database to store the metadata. The metadata may include the corresponding species name, phylogenetic
   tree, MD5 hash, and genome overview
3. Define a set of interactive interfaces
"""

