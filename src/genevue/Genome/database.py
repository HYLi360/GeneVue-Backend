class GenomeDatabase:
    def __init__(
        self,
        dbdir: Path,
        seq2bgz: bool = False,
        gff2parquet: bool = False,
        autogen_cds_and_pep: bool = False,
        internet_connection: bool = False,
        force_to_create_dir: bool = False,
        force_to_rebuild_db: bool = False,
    ):
        self.dbdir = dbdir
        self.seq2bgz = seq2bgz
        self.gff2parquet = gff2parquet
        self.autogen_cds_and_pep = autogen_cds_and_pep
        self.internet_connection = internet_connection
        self.force_to_create_dir = force_to_create_dir
        self.force_to_rebuild_db = force_to_rebuild_db

    def create(self):
        if not self.force_to_create_dir and not self.dbdir.exists():
            console.exception(DirNotExistsError)

        if self.force_to_create_dir:
            dbdir_parent_list: list[Path] = list(self.dbdir.parents)
            dbdir_parent_list.reverse()
            dbdir_parent_list.append(self.dbdir)
            for parent_dir in dbdir_parent_list:
                parent_dir.mkdir(exist_ok=True)

        if list(self.dbdir.glob("**/*")):
            if "" in list(self.dbdir.glob("**/*")):
                console.exception()
            elif not self.force_to_rebuild_db:
                console.exception(DirNotEmptyError)
            else:
                # delete all data
                shutil.rmtree(self.dbdir)
                os.mkdir(self.dbdir)

        # create start
        # inject metadata
        default_metadata = {
            "dbver": 0,
            "dbpath": str(self.dbdir),
            "flags": {
                "seq2bgz": self.seq2bgz,
                "gff2parquet": self.gff2parquet,
                "autogen_cds_and_pep": self.autogen_cds_and_pep,
                "internet_connection": self.internet_connection,
                "force_to_rebuild_db": self.force_to_rebuild_db,
            },
            "genomes": [],
        }
        json.dump(default_metadata, open(self.dbdir / "dbmetadata.json", "w"))
        with open(self.dbdir / "DO_NOT_EDIT_MANUALLY", "w") as f:
            f.write(
                "This dir was generated by genome database module of GeneVue.\n"
                "DO NOT EDIT MANUALLY.\n"
            )

